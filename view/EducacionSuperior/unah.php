      estructura y las posibilidades organizativas y metodológicas de la modalidad a distancia, encaminadas a contribuir
      de manera contundente a la inclusión social, a partir del aprovechamiento de la modalidad en la ampliación de la
      oferta de la educación superior, con calidad y pertinencia.
    </p><br>

    <p>
      <b>Misión</b><br>
      El Sistema de Educación a Distancia en la UNAH tiene como misión contribuir al fortalecimiento de la modalidad a
      distancia en la institución y en el país, mediante la investigación, la acción pedagógica y las innovaciones
      metodológicas y didácticas, con la utilización de las tecnologías de la información y de la comunicación; para
      fomentar, acompañar y generar una oferta educativa pertinente en todos los grados académicos de la educación
      superior formal así como en la educación no formal, para las comunidades locales, regionales, nacionales e
      internacionales, ampliando la oportunidad de acceso a la educación superior con calidad.
    </p>
    <br>
    <p>
      <b>Visión</b><br>
      El Sistema de Educación a Distancia de la UNAH liderará las nuevas tendencias de la educación a distancia en la
      UNAH, constituyéndose en un referente nacional e internacional en Educación a Distancia; caracterizado por su
      calidad, pertinencia, innovación, democratización, inclusión social y formación de profesionales con alto nivel
      académico y compromiso ético social, el desarrollo de una investigación rigurosa y pertinente y una vinculación
      con la sociedad que contribuya al desarrollo humano sostenible regional y nacional.
