            <img src="/img/ImagenesSiguatepeque/iglasiasp640px.jpg" alt="iglesia" style="float: right;" class="imagenes">
            El actual emplazamiento de <b>Siguatepeque</b> era habitado por indígenas de la
            etnia Lenca, que vivían de casas de paja yen la ribera de los ríos y
            riachuelos. De este grupo humano han sido hallados algunos utensilios
            como piedras de moler, vasijas, jarrones, ollas, objetos de adornos para
            sala, imágenes de deidades de diferentes formas y tamaños.
        </div>
        <br>
        <div class="contenido">
            El <b>7 de diciembre de 1537</b>, el adelantado Alonso de Cáceres recorrió el
            poblado con sus huestes españolas, encontrando indígenas habitando en las
            faldas y sabanas aledañas al cerro Calanterique y también en las orillas
            de los riachuelos: Guique y Guaratoro. Según el Padre Vallejo; Siguatepeque
            se cree fue fundada inicialmente por indios de origen Lenca, por el año de 1689.
            Ya para el año de 1788, Siguatepeque formaba parte de un curato de seis cofradías
            y contaba con las siguientes aldeas: Jaitique, Meambar y Taulabé. Hay dos padrones
            del curato de Siguatepeque en el Archivo Eclesiástico de Comayagua, con fecha de 1796.
        </div>
        <br>
        <div class="contenido">
            El libro de registro más antiguo de la época que se ha encontrado data de 1850, formando este,
            parte del archivo de la municipalidad. El 14 de abril de 1861 se le dio a Siguatepeque la
            categoría de municipio del departamento de Comayagua.Inicialmente se le dio el nombre de San José
            de Siguatepeque. Para el año de 1889 pierde como municipio la aldea de meambar, la que se
            constituye a su vez en municipio. Ese mismo año (1889), Siguatepeque se convierte en capital
            religiosa del país al instalarse en el lugar el Obispo de Honduras, Fray Manuel Francisco Vélez,
            quien había adquirido el inmueble del que fue el antiguo edificio del Instituto Genaro Muñoz
            Hernández, el cual era en esos tiempos, casa de retiro para los sacerdotes de su parroquia.
