      centro de convenciones como respuesta a múltiples y reiteradas solicitudes por parte de clientes
      y empresas locales y nacionales de obtener este tipo de servicios en un local que posee condiciones
      muy agradables brindadas por la naturaleza y atención profesional con una mejora continua en lo que
