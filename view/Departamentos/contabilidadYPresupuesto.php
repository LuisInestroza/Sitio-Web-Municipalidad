      <p class="texto">CONTABILIDAD Y PRESUPUESTOS</p>
      <img src="http://municipalidaddesiguatepeque.com/wp-content/uploads/2016/01/conyabilidad.jpg" alt="" class="imagenDepto">

      <ol type="1" class="contenido">
         <li>Desarrollar, implantar y dar seguimiento a un sistema contable y presupuestario
            de ingresos y egresos que refleje oportuna y confiable mente la situación
            económica y financiera de la Municipalidad</li>
         <li>Elaborar y presentar mensual mente al alcalde Municipal, los informes
            financieros: balance general, estado de resultado y balanza de comprobación
            de la Municipalidad.</li>
         <li> Elaborar informes mensuales sobre la ejecución y control del presupuesto y
            presentarlos al alcalde municipal.</li>
         <li>Recibir resumen de ingresos y egresos diariamente de la tesorería para
            efectuar los registros contables y presupuestarios y determinar las
            disponibilidades presupuestarias y los estados financieros en contabilidad
            presupuestaria y contabilidad financiera respectiva mente.</li>
         <li>Verificar, revisar tanto en suma, código y descripción los informes diarios de
            ingresos enviados por tesorería junto con los avisos de pagos extendidos por la
            unidad de control tributario para su respectiva contabilización.</li>
         <li>Elaboración del cuadro de ingresos mensuales.</li>
         <li>Elaboración del cuadro mensual de egresos por partida, programa y actividad.</li>
         <li> Verificar la aplicación de la codificación presupuestaria y de las normas de
            administración presupuestaria.</li>
         <li>Velar por el cumplimiento de las estipulaciones establecidas en la estructura
            presupuestaria de los ingresos y egresos, codificación, nomenclatura,
            clasificación, aprobación, modificación, limites de proceso de ejecución,
            liquidación y otras disposiciones presupuestarias (articulo 93 al 98 de la ley de
            Municipalidades y 168 al 185 de su reglamento vigente).</li>
         <li>Llevar registro de las diferentes órdenes de compra emitidas tanto al crédito
            como al contado, así como las órdenes de pago y procesarlas.</li>
         <li>Preparar la liquidación del presupuesto al final del periodo fiscal y presentar al
            Tribunal Superior de Cuentas la rendición de cuentas, está en la obligación de
            llevarlo mensualmente y los comparativos de contabilidad financiera y
            contabilidad presupuestaria en ingresos y egresos.</li>
         <li>Coordinar con el Departamento de Obras y Servicios Públicos las aplicaciones
            contables sobre obras en ejecución, sus liquidaciones finales, así como el
            control sistemático de los ingresos y egresos por concepto de prestación de
            servicios municipales.</li>
         <li>Velar porque los fondos de la Municipalidad se utilicen para el fin
            presupuestado, así como desglose de cuotas, creaciones de reglones y de
            otros aspectos presupuestarios.</li>
         <li>Participar en forma activa y dar a conocer a todas las dependencias
            municipales, los instructivos correspondientes para la elaboración del
            anteproyecto de presupuesto por programas de acuerdo a las regulaciones que
            establece la Ley de Municipalidades y su Reglamento, observando los
            procedimientos administrativos contenidos en la Guía de Formulación,
            Administración y Control Presupuestaria, que se elaborara para tales
            propósitos.</li>
         <li>Efectuar todo lo relativo en materia contable en cuentas separadas, sobre la
            recuperación de los proyectos mediante contribución por mejoras.</li>
         <li>Elaborar al final de cada ejercicio fiscal los estados de liquidación de los
            ingresos y egresos municipales y otros exigidos por la Ley de Municipalidades y
            demás aplicables</li>
      </ol>

